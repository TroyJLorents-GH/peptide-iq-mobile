// Lookup helpers
export function getCompoundById(id: string): Compound | undefined {
  return compounds.find(c => c.id === id);
}

export function getApprovedCompounds(): Compound[] {
  return compounds.filter(c => c.approvalStatus === 'approved' || c.approvalStatus === 'phase-3');
}

export function getExperimentalCompounds(): Compound[] {
  return compounds.filter(c => c.approvalStatus === 'experimental');
}

export function getNonPeptideCompounds(): Compound[] {
  return compounds.filter(c => c.category === 'non-peptide');
}

export function getBlendCompounds(): Compound[] {
  return compounds.filter(c => c.category === 'blend');
}
